package com.skillsbuild;

public class ComparisonOperators05 {

	public static void main(String[] args) {

		String myVar1 = "Java", myVar2 = "Java";

		boolean result = (myVar1 == myVar2);

		System.out.println("myVar1 == myVar2 is " + (myVar1 == myVar2));

		System.out.println("result is " + result);

		System.out.println("myVar1 != myVar2 is " + (myVar1 != myVar2));

		System.out.println("myVar1.equals(myVar2) is " + myVar1.equals(myVar2));		

	}

}				